// --------------- Translation map ---------------

export const translations: Record<Locale, typeof en> = { en, es };

// --------------- Type-safe nested key access ---------------

type NestedKeys<T> = T extends object
  ? { [K in keyof T & string]: T[K] extends object ? `${K}.${NestedKeys<T[K]>}` : K }[keyof T & string]
  : never;

export type TranslationKey = NestedKeys<typeof en>;

/** Resolve a dot-separated key on the translations object. */
export function getTranslation(locale: Locale, key: string): string {
  const parts = key.split('.');
  let obj: unknown = translations[locale];
  for (const part of parts) {
    if (obj && typeof obj === 'object' && part in obj) {
      obj = (obj as Record<string, unknown>)[part];
    } else {
      // Fallback to English, then return key itself
      let fallback: unknown = translations.en;
      for (const p of parts) {
        if (fallback && typeof fallback === 'object' && p in fallback) {
          fallback = (fallback as Record<string, unknown>)[p];
        } else {
          return key; // key not found at all
        }
      }
      return typeof fallback === 'string' ? fallback : key;
    }
  }
  return typeof obj === 'string' ? obj : key;
}
